/** 
 * Sorts digit names
 * 
 * @param {array} arr
 * @return {array}
 * 
 * @example
 *   [] => []
 *   [ 'nine','one' ]                 => [ 'one', 'nine' ]
 *   [ 'one','two','three' ]          => [ 'one','two', 'three' ]
 *   [ 'nine','eight','nine','eight'] => [ 'eight','eight','nine','nine']
 *   [ 'one','one','one','zero' ]     => [ 'zero','one','one','one' ]
 */
function sortDigitNamesByNumericOrder(arr) {
   function getKey(str) {
		switch (str) {
			case 'zero': return 0;
			case 'one':	return 1;
			case 'two': return 2;
			case 'three': return 3;
			case 'four': return 4;
			case 'five': return 5;
			case 'six': return 6;
			case 'seven': return 7;
			case 'eight': return 8;
			case 'nine': return 9;	
			}
   };
   
   arr = arr.sort(function compareNumbers(a, b) {
      return getKey(a) - getKey(b);
   })
   return arr;
}

